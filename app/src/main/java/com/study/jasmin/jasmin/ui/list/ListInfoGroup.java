package com.study.jasmin.jasmin.ui.list;

public class ListInfoGroup {
    private int no;
    private String name;
    private int photo;

    public ListInfoGroup(int no, String name, int photo) {
        this.no = no;
        this.name = name;
        this.photo = photo;
    }

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }
}




